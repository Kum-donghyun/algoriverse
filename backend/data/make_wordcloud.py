# -------------------------
# 2) 뉴스 텍스트 정제
# -------------------------
def extract_text(item):
    title = re.sub(r"<.*?>", "", item.get("title", ""))
    desc = re.sub(r"<.*?>", "", item.get("description", ""))
    return title + " " + desc

# -------------------------
# 3) 단순 한국어 토큰화
# -------------------------
def tokenize(text):
    # 한글만 추출
    words = re.findall(r"[가-힣]{2,}", text)
    words = [w for w in words if w not in stopwords]
    return words

# -------------------------
# 4) JSON 읽기
# -------------------------
NEWS_FILE = "news_500.json"

if not os.path.exists(NEWS_FILE):
    print(f"❌ ERROR: {NEWS_FILE} 파일이 존재하지 않습니다.")
    exit()

with open(NEWS_FILE, "r", encoding="utf-8") as f:
    news_items = json.load(f)

print(f"Loaded {len(news_items)} news items")

# -------------------------
# 5) 전체 뉴스 단어 추출
# -------------------------
all_words = []

for item in news_items:
    text = extract_text(item)
    nouns = tokenize(text)
    all_words.extend(nouns)

# -------------------------
# 6) 단어 빈도 계산
# -------------------------
counter = Counter(all_words)
top_words = counter.most_common(80)

print(f"Extracted {len(top_words)} frequent words")

# -------------------------
# 7) 워드클라우드 JSON 저장
# -------------------------
OUTPUT_FILE = "./public/wordcloud.json"
os.makedirs("public", exist_ok=True)

with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
    json.dump(top_words, f, ensure_ascii=False, indent=2)

print(f"워드클라우드 데이터 생성 완료 → {OUTPUT_FILE}")
